import { Link, useLocation } from 'react-router-dom';

interface NavItem {
  to: string;
  label: string;
  icon: React.ReactNode;
}

interface BottomNavProps {
  items: NavItem[];
  className?: string;
}

export const BottomNav = ({ items, className = '' }: BottomNavProps) => {
  const location = useLocation();

  return (
    <nav
      className={`fixed bottom-0 left-1/2 -translate-x-1/2 w-full max-w-[430px] bg-white border-t border-border-light z-50 ${className}`}
    >
      <div className="flex justify-around py-3">
        {items.map((item) => {
          const isActive = location.pathname === item.to;

          return (
            <Link
              key={item.to}
              to={item.to}
              className={`flex flex-col items-center gap-1 text-xs transition-colors ${
                isActive ? 'text-primary-start' : 'text-text-muted'
              }`}
            >
              <span className="w-6 h-6">{item.icon}</span>
              <span>{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
};

